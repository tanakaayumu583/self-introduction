package jp.co.aforce.sample.practice;

public class Relational {
	public static void main(String[] args) {

		int a = 11;
		int b = 3;

		System.out.println("a > b = " + (a > b));

		System.out.println("a < b = " + (a < b));

		System.out.println("a >= b = " + (a >= b));

		System.out.println("a <= b = " + (a <= b));

		System.out.println("b == a = " + (b == a));

		System.out.println("a != b = " + (a != b));
	}

}
